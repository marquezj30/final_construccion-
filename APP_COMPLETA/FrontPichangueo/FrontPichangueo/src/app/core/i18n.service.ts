@Injectable({ providedIn: 'root' })
export class I18nService {
  readonly languages = LANGUAGE_OPTIONS;
  readonly currentLanguage = signal<AppLanguage>(this.readStoredLanguage());

  constructor() {
    effect(() => {
      const language = this.currentLanguage();
      localStorage.setItem(STORAGE_KEY, language);
      document.documentElement.lang = this.locale;
    });
  }

  get locale(): string {
    return this.currentOption.locale;
  }

  get currentOption(): LanguageOption {
    return this.languages.find((language) => language.code === this.currentLanguage()) ?? this.languages[0];
  }

  setLanguage(language: AppLanguage): void {
    this.currentLanguage.set(language);
  }

  translate(key: string): string {
    if (this.currentLanguage() === 'es') return key;
    return TRANSLATIONS[this.currentLanguage()][key] ?? key;
  }

  private readStoredLanguage(): AppLanguage {
    const stored = localStorage.getItem(STORAGE_KEY) as AppLanguage | null;
    return this.languages.some((language) => language.code === stored) ? stored! : 'es';
  }
}
